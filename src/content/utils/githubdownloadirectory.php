<h1>GitHub Download Directory</h1>

<p>
This online utility allows downloading a GitHub directory, without downloading the entire repository.
This is particularly useful for downloading a single simulation from the <a href="https://github.com/ComputingMongoose/Simulations" target="_blank">Java System Simulator Simulations</a> repository.
</p>

<p>
The tool can be accessed here: <a href="https://download-directory.github.io/" target="_blank">https://download-directory.github.io/</a>.
</p>

<p>The most basic usage is to enter the URL associated with a folder from a GitHub repository. For advanced usage, it is possible to add a GitHub token, primarily useful to voercome rate limitations.</p>

<p>For other GitHub folder downloading tools, see this <a href="https://stackoverflow.com/questions/7106012/download-a-single-folder-or-directory-from-a-github-repo" target="_blank">discussion</a>.</p>