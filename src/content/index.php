<h2>Java System Simulator</h2>
<p>This project dedicated to simulating old computers. The simulator is written in Java. For certain devices, images are used to re-create part of the look and feel of the old hardware. Currently, it is possible to execute ROMs from Intellec systems, with different processors, and run CP/M (possibly other systems as well). By combining different CPUs and memory options, it should be possible to simulate other systems as well.</p>
