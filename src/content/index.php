<h1>About this Web Site</h1>
<p>This website is dedicated to my various projects, especially the Java System Simulator project.
In addition, it contains information and project notes that I considered to be relevant during my work. 
I feel that simulating old computer systems is a way to understand the history of computers and to revive old ideas and software.
Sometimes not all that is old is also obsolete. Certain old ideas may come to life again under a new technological approach.
In an era of almost daily Windows automatic updates it may be worth investigating how old computers could run without a single update.
It's also interesting to see how kilobytes of memory lead to highly optimized code, while today's gigabytes of memory perhaps contribute towards slow and sloppy-written programs.</p>

