<h2>Main Characteristics</h2>
<ul>
<li>Speed up to 100 words/minute (60 wpm, 66 wpm, 100 wpm)</li>
<li>Four-row keyboard generating an eight-level code</li>
<li>The SHIFT key is used to transmit printing characters (such as &amp;, %, and #)</li>
<li>The CTRL key is used to transmit control functions (such as WRU, BELL, and TAB)</li>
<li>Transmission code: 8-level start-stop signals with 11-unit transmission pattern</li>
<li>Paper 8-1/2 inches wide; max 5 inch diameter roll</li>
<li>10 characters/inch, max 74 characters/line</li>
<li>Uppercase characters only</li>
<li>Tape specifications: 5 or 8 levels; width 11/16 inch (17 mm) for 5 levels, 1 inch (25 mm) for 8 levels; 10 code combinations per inch; feed hole diameter 0.0465 inch</li>
<li>Communication via current loop (a modern RS-232 adapter card exists; details <a href="https://deramp.com/tty_adapter.html" target="_blank">here</a>)</li>
<li>
</ul>



