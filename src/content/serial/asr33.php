<h1>ASR-33 Teletype</h1>

<p>The Teletype Corporation introduced the Model 33 in 1963. There were 3 versions: Model 33 ASR (Automatic Send Receive) which includes a paper tape reader and puncher; Model 33 KSR (Keyboard Send Receive) which lacks the paper tape reader/puncher; Model 33 RO (Receive Only) which lacks both the paper tape reader/puncher and the keyboard. The Model 33 was one of the first products to employ the ASCII code.
</p>


